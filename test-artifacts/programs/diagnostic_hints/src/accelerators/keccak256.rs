use zkvm_interface::{zkvm_keccak256, zkvm_keccak256_hash, zkvm_status_ZKVM_EOK as ZKVM_EOK};

pub fn diagnostic_zkvm_keccak256() {
    // Test #0: empty input
    let data: [u8; 0] = [];
    let mut output = zkvm_keccak256_hash { data: [0u8; 32] };
    let status = unsafe { zkvm_keccak256(data.as_ptr(), data.len(), &mut output) };
    assert_eq!(status, ZKVM_EOK);
    let expected: [u8; 32] = [
        0xc5, 0xd2, 0x46, 0x01, 0x86, 0xf7, 0x23, 0x3c, 0x92, 0x7e, 0x7d, 0xb2, 0xdc, 0xc7, 0x03,
        0xc0, 0xe5, 0x00, 0xb6, 0x53, 0xca, 0x82, 0x27, 0x3b, 0x7b, 0xfa, 0xd8, 0x04, 0x5d, 0x85,
        0xa4, 0x70,
    ];
    assert_eq!(output.data, expected);

    // Test #1: "abc"
    let data: &[u8] = b"abc";
    let mut output = zkvm_keccak256_hash { data: [0u8; 32] };
    let status = unsafe { zkvm_keccak256(data.as_ptr(), data.len(), &mut output) };
    assert_eq!(status, ZKVM_EOK);
    let expected: [u8; 32] = [
        0x4e, 0x03, 0x65, 0x7a, 0xea, 0x45, 0xa9, 0x4f, 0xc7, 0xd4, 0x7b, 0xa8, 0x26, 0xc8, 0xd6,
        0x67, 0xc0, 0xd1, 0xe6, 0xe3, 0x3a, 0x64, 0xa0, 0x36, 0xec, 0x44, 0xf5, 0x8f, 0xa1, 0x2d,
        0x6c, 0x45,
    ];
    assert_eq!(output.data, expected);

    // Test #2: 135-byte input (one byte short of the 136-byte Keccak-256 rate;
    // exercises single-block padding at the boundary)
    let data: [u8; 135] = [0xab; 135];
    let mut output = zkvm_keccak256_hash { data: [0u8; 32] };
    let status = unsafe { zkvm_keccak256(data.as_ptr(), data.len(), &mut output) };
    assert_eq!(status, ZKVM_EOK);
    let expected: [u8; 32] = [
        0x93, 0x2f, 0xed, 0xc0, 0xe8, 0x54, 0xcc, 0x4d, 0x32, 0xee, 0xc6, 0x9e, 0x89, 0x6c, 0x74,
        0x49, 0x57, 0x00, 0x52, 0xb3, 0xaa, 0xce, 0xac, 0xff, 0x7b, 0x13, 0x74, 0x53, 0x25, 0xe4,
        0xcf, 0x47,
    ];
    assert_eq!(output.data, expected);

    // Test #3: 136-byte input (exactly one block; exercises multi-block padding)
    let data: [u8; 136] = [0xab; 136];
    let mut output = zkvm_keccak256_hash { data: [0u8; 32] };
    let status = unsafe { zkvm_keccak256(data.as_ptr(), data.len(), &mut output) };
    assert_eq!(status, ZKVM_EOK);
    let expected: [u8; 32] = [
        0x30, 0x2d, 0xb7, 0x3a, 0x4c, 0x8c, 0xc8, 0xec, 0xc9, 0x00, 0x4f, 0xec, 0x3a, 0x65, 0x25,
        0xd9, 0xd6, 0xa2, 0xdd, 0x4b, 0x09, 0x8b, 0x1b, 0xf6, 0x2d, 0x1b, 0x89, 0x7a, 0xcf, 0xf1,
        0x8c, 0x9d,
    ];
    assert_eq!(output.data, expected);

    // Test #4: 137-byte input (one byte more than one block; exercises multi-block padding)
    let data: [u8; 137] = [0xab; 137];
    let mut output = zkvm_keccak256_hash { data: [0u8; 32] };
    let status = unsafe { zkvm_keccak256(data.as_ptr(), data.len(), &mut output) };
    assert_eq!(status, ZKVM_EOK);
    let expected: [u8; 32] = [
        0x02, 0x35, 0xd1, 0x4c, 0xb2, 0x56, 0x3b, 0xe9, 0xd3, 0x00, 0xa2, 0x6a, 0xa4, 0xdd, 0x02,
        0xe3, 0x7e, 0x51, 0xb8, 0x02, 0xb1, 0xb2, 0x04, 0x69, 0x1d, 0xb2, 0xde, 0x6a, 0x32, 0x9d,
        0x79, 0x48,
    ];
    assert_eq!(output.data, expected);

    // Test #5: 272-byte input (two blocks; exercises multi-block padding)
    let data: [u8; 272] = [0xcd; 272];
    let mut output = zkvm_keccak256_hash { data: [0u8; 32] };
    let status = unsafe { zkvm_keccak256(data.as_ptr(), data.len(), &mut output) };
    assert_eq!(status, ZKVM_EOK);
    let expected: [u8; 32] = [
        0x82, 0xb8, 0x61, 0xac, 0x32, 0x7c, 0xd7, 0x5d, 0x75, 0x03, 0x16, 0xc8, 0xd3, 0x5e, 0xc3,
        0x6d, 0xec, 0x8d, 0x6e, 0xb0, 0x49, 0x28, 0x9f, 0xa8, 0xcc, 0x36, 0x38, 0xf9, 0x0d, 0x42,
        0x8e, 0xfc,
    ];
    assert_eq!(output.data, expected);
}
