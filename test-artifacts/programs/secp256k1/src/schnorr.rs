pub fn schnorr_tests() {
    // ==================== schnorr_verify_secp256k1 ====================

    // Valid: BIP-340 vector 0
    assert!(schnorr_verify_secp256k1(&MSG0, &PK0_X, &SIG0_R, &SIG0_S));

    // Valid: BIP-340 vector 1 (different key + non-zero message)
    assert!(schnorr_verify_secp256k1(&MSG1, &PK1_X, &SIG1_R, &SIG1_S));

    // Invalid: tweak the last byte of `s` from vector 0
    let mut sig0_s_bad = SIG0_S;
    sig0_s_bad[31] ^= 1;
    assert!(!schnorr_verify_secp256k1(&MSG0, &PK0_X, &SIG0_R, &sig0_s_bad));

    // ==================== schnorr_batch_verify_secp256k1 ====================

    // Valid: batch of 3 BIP-340 vectors
    let msgs: &[&[u8]] = &[&MSG0, &MSG1, &MSG2];
    let pk_xs: &[&[u8; 32]] = &[&PK0_X, &PK1_X, &PK2_X];
    let sig_rs: &[&[u8; 32]] = &[&SIG0_R, &SIG1_R, &SIG2_R];
    let sig_ss: &[&[u8; 32]] = &[&SIG0_S, &SIG1_S, &SIG2_S];
    assert!(schnorr_batch_verify_secp256k1(msgs, pk_xs, sig_rs, sig_ss));

    // Invalid: corrupt the middle signature's `s`
    let mut sig1_s_bad = SIG1_S;
    sig1_s_bad[31] ^= 1;
    let sig_ss_bad: &[&[u8; 32]] = &[&SIG0_S, &sig1_s_bad, &SIG2_S];
    assert!(!schnorr_batch_verify_secp256k1(msgs, pk_xs, sig_rs, sig_ss_bad));

    // Stress tests
    // Runs the batch verification once for each power of two from `2^0 = 1` up to `2^13 = 8192`.
    for power in 0..=13 {
        println!("Running schnorr_batch_verify_secp256k1 with batch size = {u}", u = 1usize << power);
        ziskos::profile_start!(COST_schnorr_batch_verify_secp256k1);
        ziskos::profile_steps_start!(STEPS_schnorr_batch_verify_secp256k1);
        let u = 1usize << power;
        run_batch_at_size(u);
        ziskos::profile_steps_end!(STEPS_schnorr_batch_verify_secp256k1);
        ziskos::profile_end!(COST_schnorr_batch_verify_secp256k1);
    }
}

#[inline(never)]
fn run_batch_at_size(u: usize) {
    let mut msgs: Vec<&[u8]> = Vec::with_capacity(u);
    let mut pks: Vec<&[u8; 32]> = Vec::with_capacity(u);
    let mut rs: Vec<&[u8; 32]> = Vec::with_capacity(u);
    let mut ss: Vec<&[u8; 32]> = Vec::with_capacity(u);
    for i in 0..u {
        match i % 3 {
            0 => {
                msgs.push(&MSG0);
                pks.push(&PK0_X);
                rs.push(&SIG0_R);
                ss.push(&SIG0_S);
            }
            1 => {
                msgs.push(&MSG1);
                pks.push(&PK1_X);
                rs.push(&SIG1_R);
                ss.push(&SIG1_S);
            }
            _ => {
                msgs.push(&MSG2);
                pks.push(&PK2_X);
                rs.push(&SIG2_R);
                ss.push(&SIG2_S);
            }
        }
    }
    assert!(schnorr_batch_verify_secp256k1(&msgs, &pks, &rs, &ss));
}