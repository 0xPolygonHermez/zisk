// Several invalid-vectors share data with vector 5; alias for readability in the assertions.
const PK_1_THRU_13: [u8; 32] = PK_1; // vecs 6..=13 all reuse PK_1
const MSG_1_THRU_13: [u8; 32] = MSG_1; // vecs 6..=14 all reuse MSG_1
const R_REUSED: [u8; 32] = R_5; // R for vecs 8, 13, 14
const S_REUSED: [u8; 32] = S_5; // S for vecs 11, 12, 14

pub fn schnorr_tests() {
    // ==================== schnorr_verify_secp256k1: all BIP-340 vectors ====================

    // Valid vectors (0-4).
    assert!(schnorr_verify_secp256k1(&MSG_0, &PK_0, &R_0, &S_0));
    assert!(schnorr_verify_secp256k1(&MSG_1, &PK_1, &R_1, &S_1));
    assert!(schnorr_verify_secp256k1(&MSG_2, &PK_2, &R_2, &S_2));
    assert!(schnorr_verify_secp256k1(&MSG_3, &PK_3, &R_3, &S_3));
    assert!(schnorr_verify_secp256k1(&MSG_4, &PK_4, &R_4, &S_4));

    // Invalid vectors (5-14): all share PK_1/MSG_1 except vec 5 (own PK) and vec 14 (bad PK).
    assert!(!schnorr_verify_secp256k1(&MSG_1, &PK_5, &R_5, &S_5)); // vec 5: PK off-curve
    assert!(!schnorr_verify_secp256k1(&MSG_1_THRU_13, &PK_1_THRU_13, &R_6, &S_6)); // vec 6: R has odd y
    assert!(!schnorr_verify_secp256k1(&MSG_1_THRU_13, &PK_1_THRU_13, &R_7, &S_7)); // vec 7: negated msg sig
    assert!(!schnorr_verify_secp256k1(&MSG_1_THRU_13, &PK_1_THRU_13, &R_REUSED, &S_8)); // vec 8: negated s
    assert!(!schnorr_verify_secp256k1(&MSG_1_THRU_13, &PK_1_THRU_13, &R_9, &S_9)); // vec 9: sG-eP = O, r=0
    assert!(!schnorr_verify_secp256k1(&MSG_1_THRU_13, &PK_1_THRU_13, &R_10, &S_10)); // vec 10: sG-eP = O, r=1
    assert!(!schnorr_verify_secp256k1(&MSG_1_THRU_13, &PK_1_THRU_13, &R_11, &S_REUSED)); // vec 11: r not on curve
    assert!(!schnorr_verify_secp256k1(&MSG_1_THRU_13, &PK_1_THRU_13, &R_12, &S_REUSED)); // vec 12: r == field size
    assert!(!schnorr_verify_secp256k1(&MSG_1_THRU_13, &PK_1_THRU_13, &R_REUSED, &S_13)); // vec 13: s == curve order
    assert!(!schnorr_verify_secp256k1(&MSG_1, &PK_14, &R_REUSED, &S_REUSED)); // vec 14: PK x > field size

    // Variable-length-message vectors (15-18), all with the same key.
    assert!(schnorr_verify_secp256k1(&MSG_15, &PK_VAR, &R_15, &S_15)); // msg = 0 bytes
    assert!(schnorr_verify_secp256k1(&MSG_16, &PK_VAR, &R_16, &S_16)); // msg = 1 byte
    assert!(schnorr_verify_secp256k1(&MSG_17, &PK_VAR, &R_17, &S_17)); // msg = 17 bytes
    assert!(schnorr_verify_secp256k1(&MSG_18, &PK_VAR, &R_18, &S_18)); // msg = 100 bytes

    // ==================== schnorr_batch_verify_secp256k1 ====================

    // Valid: batch of 3 BIP-340 vectors (0, 1, 2).
    let msgs: &[&[u8]] = &[&MSG_0, &MSG_1, &MSG_2];
    let pks: &[&[u8; 32]] = &[&PK_0, &PK_1, &PK_2];
    let rs: &[&[u8; 32]] = &[&R_0, &R_1, &R_2];
    let ss: &[&[u8; 32]] = &[&S_0, &S_1, &S_2];
    assert!(schnorr_batch_verify_secp256k1(msgs, pks, rs, ss));

    // Invalid: corrupt the middle signature's `s`.
    let mut s1_bad = S_1;
    s1_bad[31] ^= 1;
    let ss_bad: &[&[u8; 32]] = &[&S_0, &s1_bad, &S_2];
    assert!(!schnorr_batch_verify_secp256k1(msgs, pks, rs, ss_bad));

    // Batch with mixed message lengths (vectors 15, 16, 17, 18).
    let msgs_var: &[&[u8]] = &[&MSG_15, &MSG_16, &MSG_17, &MSG_18];
    let pks_var: &[&[u8; 32]] = &[&PK_VAR, &PK_VAR, &PK_VAR, &PK_VAR];
    let rs_var: &[&[u8; 32]] = &[&R_15, &R_16, &R_17, &R_18];
    let ss_var: &[&[u8; 32]] = &[&S_15, &S_16, &S_17, &S_18];
    assert!(schnorr_batch_verify_secp256k1(msgs_var, pks_var, rs_var, ss_var));

    // Stress / benchmark: batch verification at each power of two from 2^0 = 1 up to 2^13 = 8192.
    for power in 0..=13 {
        let u = 1usize << power;
        run_batch_at_size(u);
    }
}

#[inline(never)]
fn run_batch_at_size(u: usize) {
    let mut msgs: Vec<&[u8]> = Vec::with_capacity(u);
    let mut pks: Vec<&[u8; 32]> = Vec::with_capacity(u);
    let mut rs: Vec<&[u8; 32]> = Vec::with_capacity(u);
    let mut ss: Vec<&[u8; 32]> = Vec::with_capacity(u);
    for i in 0..u {
        match i % 3 {
            0 => {
                msgs.push(&MSG_0);
                pks.push(&PK_0);
                rs.push(&R_0);
                ss.push(&S_0);
            }
            1 => {
                msgs.push(&MSG_1);
                pks.push(&PK_1);
                rs.push(&R_1);
                ss.push(&S_1);
            }
            _ => {
                msgs.push(&MSG_2);
                pks.push(&PK_2);
                rs.push(&R_2);
                ss.push(&S_2);
            }
        }
    }
    assert!(schnorr_batch_verify_secp256k1(&msgs, &pks, &rs, &ss));
}
